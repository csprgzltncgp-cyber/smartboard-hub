    <p>Vážený partneru!</p>
    <p>S potěšením vás informujeme, že kromě Osobních, Telefonických a Video konzultací zavádíme také nový formát konzultací – Chat.</p>
    <p>Chat konzultace je v podstatě série sezení, během kterých si odborník a klient vyměňují zprávy na platformě vytvořené speciálně pro tento účel. Chatové poradenství nabízí klientům nový a pohodlný způsob, jak požádat o pomoc a získat ji. Tento typ poradenství se v posledních letech stal stále populárnějším díky mnoha profesionálním výhodám, které ospravedlňují jeho používání:</p>
    <ul>
        <li>Klienti, kteří se cítí nepohodlně při osobních nebo online schůzkách, se mohou snadněji otevřít během písemné konverzace. Chatový formát zajišťuje diskrétnost a anonymitu, což může zvýšit důvěru klientů a jejich ochotu požádat o pomoc.</li>
        <li>Pro klienty je snadno dostupný a pohodlný, protože ho mohou využívat kdekoli a kdykoli pomocí chytrého telefonu a připojení k internetu.</li>
        <li>Nabízí okamžitou podporu a zpětnou vazbu klientům, protože umožňuje rychle a efektivně reagovat na vzniklé problémy.</li>
    </ul>
    <p><strong>Jak funguje chat konzultace?</strong></p>
    <p>Klienti se mohou přihlásit k psychologickému poradenství, včetně Chat konzultace, obvyklým způsobem s tím rozdílem, že místo osobní, telefonické nebo video konzultace zvolí novou Chat konzultaci. Přihlásit se mohou přes EAP Call Centrum, webovou stránku EAP Online a mobilní aplikaci EAP Chat.</p>
    <p>Klienti se mohou připojit k chatovému poradenství na dvou platformách: na webové stránce EAP Online a v nové mobilní aplikaci EAP Chat. Specialista se může připojit k poradenství přes Expert Dashboard na kartě konzultace pouhým stisknutím jednoho tlačítka.</p>
    <p>Stejně jako u osobního a online poradenství trvá jedno chatové sezení 50 minut a klient může využít tolik sezení, kolik jeho firma podporuje.</p>
    <p>Všechny poradenské sezení, včetně druhého, třetího atd., musí probíhat na Expert Dashboard.</p>
    <p>Stanovení termínů probíhá obvyklým způsobem, v tom není žádná změna. Před ukončením konzultace je nutné s klientem dohodnout termín dalšího sezení a zapsat ho na kartě konzultace.</p>
    <p>Věříme, že tato nová služba nám pomůže oslovit více klientů a účinně přispěje k jejich duševnímu zdraví.</p>
